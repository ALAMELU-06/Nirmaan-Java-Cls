package oops;

public class Employer {
	
	
	private String name;
	private int id;
	private String dept;
	
	public Employer () {}
	public Employer(String name) {
		this.name = name;
	}
	
	public Employer(String name, int id, String dept) {
		this.name = name;
		this.id = id;
		this.dept = dept;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getid() {
		return id;
	}

	public void setid(int id) {
		this.id = id;
	}

	public String getdept() {
		return dept;
	}

	public void setdept(String dept) {
		dept = dept;
	}
	
	
}
	
	
	
		
		
		
		
		
		

	
                             

