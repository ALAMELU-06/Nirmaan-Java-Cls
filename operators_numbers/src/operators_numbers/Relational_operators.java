package operators_numbers;

public class Relational_operators {
	public static void main(String[]args) {
		
		int p = 1;
		int q = 20;
		int r = 6;
		
		
		System.out.println("p > q:" + (p > q));                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                    
		System.out.println("p < r:" + (p < r));
		System.out.println("p >= q:" + (p >= q));
		System.out.println("p <= r:" + (p <= r));
		System.out.println("q == r:" + (q == r));
		System.out.println("q != r:" + (q !=r));
	}
}