package while_loop;

public class consecutive_while_numbers {
	 public static void main(String[] args) {

	        int start = 1;   
	        int end = 10;    

	        System.out.println("Consecutive Numbers:");

	        while (start <= end) {
	            System.out.print(start + " \n");
	            start++; 
	        }
	    }
	}

