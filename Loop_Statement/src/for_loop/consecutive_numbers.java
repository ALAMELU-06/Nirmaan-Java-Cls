package for_loop;

public class consecutive_numbers {
	 public static void main(String[] args) {

	        int start = 1;   
	        int count = 10; 

	        System.out.println("Consecutive Numbers:");
	        for (int i = 0; i < count; i++) {
	            System.out.print((start + i) + "\n ");
	        }
	    }
	}


