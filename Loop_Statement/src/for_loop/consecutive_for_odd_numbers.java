package for_loop;

public class consecutive_for_odd_numbers {
	public static void main(String[] args) {

        System.out.println("Odd Numbers:");

        for (int i = 1; i <= 5; i++) {
            if (i % 2 != 0) {   
                System.out.print(i + " \n");
            }
        }
    }
}


